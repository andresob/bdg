    <h2 class="light">Tutorial</h2>
  </div>
  <div class="container-fluid">
    <div class="row-fluid">
      <p>Ferramenta foi projetada para ser o mais simples possível. As consultas recebem um apoio das tabelas com as respectivas informações.</p>
      <p>O usuário terá a opção de fazer a consulta textualmente ou então com o auxílio dos cliques na tabela. Conforme os cliques as informações dos atributos e tabelas vão sendo inseridas no campo de consulta. O destaques dos campos ajuda a posicionar o usuário sobre quais atributos ele está realizando a consulta.</p>
      <p>Fique atento os campos da busca preenchidos são somente o de seleção e o de origem, contudo cabe ao usuário linkar as tabelas, atrelar atributos de igual valor para realizar buscas mais elaboradas.</p>
      <p>Após cada consulta o usuário será automaticamente redirecionado para a página que contém a tabela com a relação das buscas</p>
      <p>A visualização do mapa das consultas é algo fundamental para a conclusão e interpretação da base, logo é possível acessar após cada consulta o mapa gerado e ,com auxílio de ferramentas na parte lateral do site, um campo de personalização de cores.</p>
      <p>Todas as consultas feitas serão armazenadas, também, na parte lateral do site, para facilitar, mais uma vez, as ações do usuário.</p>
